
public class Comparing {
	public static void main(String args[]) {
		int x = 8, y = 4;
		System.out.println("x<y:" + (x < y));
		System.out.println("x>y:" + (x > y));
		System.out.println("x<=y:" + (x <= y));
		System.out.println("x>=y:" + (x >= y));
		System.out.println("x==y:" + (x == y));
		System.out.println("x!=y:" + (x != y));
	}
}
